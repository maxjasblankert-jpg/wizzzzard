/**
 * One-time Firebase setup for Wizard multiplayer.
 * Prerequisites: run `npm run firebase:login` first (Google account in browser).
 *
 * Creates project, Firestore, web app config, writes public/firebase-config.js,
 * and deploys Firestore rules.
 */
require('./load-env');

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const PROJECT_ID = process.env.FIREBASE_PROJECT_ID || 'wizard-f356d';
const DISPLAY_NAME = 'Wizard Multiplayer';
const CONFIG_PATH = path.join(__dirname, '..', 'public', 'firebase-config.js');
const FIREBASERC = path.join(__dirname, '..', '.firebaserc');

function run(cmd, opts = {}) {
  console.log(`> ${cmd}`);
  return execSync(cmd, { stdio: 'inherit', encoding: 'utf8', ...opts });
}

function runCapture(cmd) {
  console.log(`> ${cmd}`);
  return execSync(cmd, { encoding: 'utf8', stdio: ['pipe', 'pipe', 'inherit'] });
}

function writeFirebaseConfig(cfg) {
  const content = `// Auto-generated by scripts/setup-firebase.js
window.firebaseConfig = ${JSON.stringify(cfg, null, 2)};

window.firebaseConfigured = window.firebaseConfig.apiKey !== 'YOUR_API_KEY';
`;
  fs.writeFileSync(CONFIG_PATH, content);
  console.log(`\n✓ Wrote ${CONFIG_PATH}`);
}

function updateFirebaserc(projectId) {
  fs.writeFileSync(FIREBASERC, JSON.stringify({ projects: { default: projectId } }, null, 2) + '\n');
}

async function main() {
  try {
    runCapture('npx firebase-tools projects:list');
  } catch {
    console.error('\n❌ Not logged in. Run this first:\n   npm run firebase:login\n');
    process.exit(1);
  }

  let projectId = PROJECT_ID;

  try {
    runCapture(`npx firebase-tools projects:list --filter projectId:${projectId}`);
    console.log(`\n✓ Project "${projectId}" already exists.`);
  } catch {
    console.log(`\nCreating Firebase project "${projectId}"…`);
    run(`npx firebase-tools projects:create ${projectId} --display-name "${DISPLAY_NAME}"`);
  }

  updateFirebaserc(projectId);

  console.log('\nEnabling Firestore (Europe)…');
  try {
    run(`npx firebase-tools firestore:databases:create --project ${projectId} --location eur3`);
  } catch (e) {
    console.log('(Firestore may already exist — continuing.)');
  }

  console.log('\nCreating web app…');
  let appId;
  try {
    const out = runCapture(`npx firebase-tools apps:create WEB "Wizard Web" --project ${projectId} --json`);
    const parsed = JSON.parse(out);
    appId = parsed.result?.appId || parsed.appId;
  } catch {
    const listOut = runCapture(`npx firebase-tools apps:list WEB --project ${projectId} --json`);
    const list = JSON.parse(listOut);
    appId = list.result?.[0]?.appId;
    if (!appId) throw new Error('Could not find or create a web app.');
    console.log(`Using existing web app: ${appId}`);
  }

  const sdkOut = runCapture(`npx firebase-tools apps:sdkconfig WEB ${appId} --project ${projectId}`);
  const match = sdkOut.match(/const firebaseConfig = (\{[\s\S]*?\});/);
  if (!match) {
    throw new Error('Could not parse SDK config from Firebase CLI output.');
  }
  const cfg = eval(`(${match[1]})`);
  writeFirebaseConfig(cfg);

  console.log('\nDeploying Firestore security rules…');
  run('npx firebase-tools deploy --only firestore:rules --project ' + projectId);

  console.log(`
✅ Firebase setup complete!

Next steps (manual — Firebase Console):
1. Open https://console.firebase.google.com/project/${projectId}/authentication/providers
2. Enable "Anonymous" sign-in

Then deploy the site:
   npm run deploy:vercel

Share https://wizzzzard.vercel.app with friends — create room, share 4-letter code.
`);
}

main().catch(err => {
  console.error('\n❌ Setup failed:', err.message);
  process.exit(1);
});
